package bsoft.com.lib_filter.filter.model;

import android.graphics.Bitmap;
import android.widget.ImageView;

import java.io.Serializable;

import bsoft.com.lib_filter.filter.gpu.GPUFilterRes;

/**
 * Custom child list item.
 * <p>
 * This is for demo purposes, although it is recommended having a separate
 * child from your parent.
 * <p>
 * You do not need to implement Serializable in order to use ExpandableRecyclerView. We are doing so
 * here in order to store away our list of items (which you usually will have in a singleton or
 * database)
 */
public class FilterHorizontalChild {
    private GPUFilterRes gpuFilterRes;

    public GPUFilterRes getGpuFilterRes() {
        return gpuFilterRes;
    }

    public void setGpuFilterRes(GPUFilterRes gpuFilterRes) {
        this.gpuFilterRes = gpuFilterRes;
    }

    private Bitmap bmp;

    public Bitmap getBmp() {
        return bmp;
    }

    public void setBmp(Bitmap bmp) {
        this.bmp = bmp;
    }
}
